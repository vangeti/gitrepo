package cgt.dop.bean;

import java.util.List;

public class ResponseCodeBean {

    private long responseCode;

    private String elapsedTime;
    
    private List<Integer> respCodeCounts;
    
    private List<Long> respCodesList;

    public List<Long> getRespCodesList() {
		return respCodesList;
	}

	public void setRespCodesList(List<Long> respCodesList) {
		this.respCodesList = respCodesList;
	}

	public List<Integer> getRespCodeCounts() {
		return respCodeCounts;
	}

	public void setRespCodeCounts(List<Integer> respCodeCounts) {
		this.respCodeCounts = respCodeCounts;
	}

	public long getResponseCode() {
        return responseCode;
    }

    public void setResponseCode(long responseCode) {
        this.responseCode = responseCode;
    }

    public String getElapsedTime() {
        return elapsedTime;
    }

    public void setElapsedTime(String elapsedTime) {
        this.elapsedTime = elapsedTime;
    }

	@Override
	public String toString() {
		return "ResponseCodeBean [responseCode=" + responseCode + ", elapsedTime=" + elapsedTime + "]";
	}


}
