package cgt.dop.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "test_update")
public class TestUpdateModel {

	@Id
	@Column
	@GeneratedValue(strategy = GenerationType.AUTO)
	private int id;

	@Column
	private String comment_name;

	@Column
	private String user_name;

	@Column
	private String comment_date;
	
	@Column
	private String test_type;
	
	@Column
	private String test_num;
	
	@Column
	private String testwith;
	
	@Column
	private String testto;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getComment_name() {
		return comment_name;
	}

	public void setComment_name(String comment_name) {
		this.comment_name = comment_name;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getComment_date() {
		return comment_date;
	}

	public void setComment_date(String comment_date) {
		this.comment_date = comment_date;
	}

	public String getTest_type() {
		return test_type;
	}

	public void setTest_type(String test_type) {
		this.test_type = test_type;
	}

	public String getTest_num() {
		return test_num;
	}

	public void setTest_num(String test_num) {
		this.test_num = test_num;
	}

	public String getTestwith() {
		return testwith;
	}

	public void setTestwith(String testwith) {
		this.testwith = testwith;
	}

	public String getTestto() {
		return testto;
	}

	public void setTestto(String testto) {
		this.testto = testto;
	}
	
	
	
}
