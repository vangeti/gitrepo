package cgt.dop.alm.util;

public class DefectsTO {
	
	private String id;

	private String name;
	
	private String applicationName;
	
	private String severity;
	
	private String priority;
	
	private String status;
	
	private String defectDetectedStage;
	
	private String defectCreatedDate;
	
	private String mttd;
	
	private String detectedBy;
	

	public DefectsTO() {		
	}


	public DefectsTO(String id, String name, String applicationName,
		String severity, String priority, String status,
		String defectDetectedStage, String defectCreatedDate,
		String mttd,String detectedBy) {
	    super();
	    this.id = id;
	    this.name = name;
	    this.applicationName = applicationName;
	    this.severity = severity;
	    this.priority = priority;
	    this.status = status;
	    this.defectDetectedStage = defectDetectedStage;
	    this.defectCreatedDate = defectCreatedDate;
	    this.mttd = mttd;
	    this.detectedBy = detectedBy;
	}


	public String getId() {
	    return id;
	}


	public void setId(String id) {
	    this.id = id;
	}


	public String getName() {
	    return name;
	}


	public void setName(String name) {
	    this.name = name;
	}


	public String getApplicationName() {
	    return applicationName;
	}


	public void setApplicationName(String applicationName) {
	    this.applicationName = applicationName;
	}


	public String getSeverity() {
	    return severity;
	}


	public void setSeverity(String severity) {
	    this.severity = severity;
	}


	public String getPriority() {
	    return priority;
	}


	public void setPriority(String priority) {
	    this.priority = priority;
	}


	public String getStatus() {
	    return status;
	}


	public void setStatus(String status) {
	    this.status = status;
	}


	public String getDefectDetectedStage() {
	    return defectDetectedStage;
	}


	public void setDefectDetectedStage(String defectDetectedStage) {
	    this.defectDetectedStage = defectDetectedStage;
	}


	public String getDefectCreatedDate() {
	    return defectCreatedDate;
	}


	public void setDefectCreatedDate(String defectCreatedDate) {
	    this.defectCreatedDate = defectCreatedDate;
	}


	public String getMttd() {
	    return mttd;
	}


	public void setMttd(String mttd) {
	    this.mttd = mttd;
	}


	public String getDetectedBy() {
	    return detectedBy;
	}


	public void setDetectedBy(String detectedBy) {
	    this.detectedBy = detectedBy;
	}


	@Override
	public String toString() {
	    StringBuilder builder = new StringBuilder();
	    builder.append("DefectsTO [id=");
	    builder.append(id);
	    builder.append(", name=");
	    builder.append(name);
	    builder.append(", applicationName=");
	    builder.append(applicationName);
	    builder.append(", severity=");
	    builder.append(severity);
	    builder.append(", priority=");
	    builder.append(priority);
	    builder.append(", status=");
	    builder.append(status);
	    builder.append(", defectDetectedStage=");
	    builder.append(defectDetectedStage);
	    builder.append(", defectCreatedDate=");
	    builder.append(defectCreatedDate);
	    builder.append(", mttd=");
	    builder.append(mttd);
	    builder.append(", detectedBy=");
	    builder.append(detectedBy);
	    builder.append("]");
	    return builder.toString();
	}
	
	
}
